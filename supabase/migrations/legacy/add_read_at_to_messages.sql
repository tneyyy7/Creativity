-- =============================================================================
-- Время прочтения для ЛИЧНЫХ сообщений ("Прочитано · ЧЧ:ММ").
-- Запустить ОДИН РАЗ в Supabase Dashboard → SQL Editor → New query → Run.
-- (Через публичный anon-ключ из приложения ALTER TABLE выполнить нельзя.)
-- =============================================================================

-- 1) Колонка, в которую markAsRead() записывает момент прочтения.
ALTER TABLE messages ADD COLUMN IF NOT EXISTS read_at TIMESTAMPTZ;

-- 2) (Необязательно) Заполнить уже прочитанные личные сообщения временем отправки,
--    чтобы в старых перепиписках тоже сразу показывалось время. Для новых
--    сообщений время будет точным (фактический момент прочтения). Если нужна
--    только точность для будущих сообщений — этот шаг можно пропустить.
UPDATE messages
SET read_at = created_at
WHERE is_read = true
  AND read_at IS NULL
  AND group_id IS NULL;
